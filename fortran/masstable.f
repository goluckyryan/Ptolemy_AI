C
C  Modifications of the Speakeasy HOWBOUND subroutines for Ptolemy
C
C    The following changes are made:
c
c     EXCESS - allow Z = 0 or Z = A for and return Z*excess
C       of a proton for calls with Z=A so that multi-proton
c       cluster transfers will receive a valid result in the dwba code.
c                                                                          10   
c     AZCODE - recgonizes  p, n, d, etc.
C
C    EXCESS - returns mass excess as tabulated in 1993
C             mass adjustment.
C
C--------------------------------------------------------
C
      FUNCTION EXCESS(KZ,KA,NOTTAB)
      REAL*8 excess, EX, ER
C                                                                          20   
C        Get excess from table with MASEXX
C
c   .... check for xn or gamma (z=0 n>=0 .. these are ok)
      if ( kz .eq. 0 .and. ka .ge. 0 ) go to 10
c   .... check for yp  (z=a n=0...these are also ok  1/76 )
      if ( kz .eq. ka ) go to 20
c
      CALL MASEXX(KZ, KA, EX, ER, NOTTAB)
      excess = 1.0D-3 * EX
C                                                                          30   
      RETURN
C
c ----  return for neutral particles  (xn and gamma)
 10   call masexx ( 0, 1, ex, er, nottab )
      excess  = 1.0d-3 * ka*ex
      return
c
c ----  return for y-proton cluster energies
 20   call masexx ( 1, 1, ex, er, nottab )
      excess  = 1.0d-3 * ka*ex                                             40   
      return
C
      END
