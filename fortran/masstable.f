C   IRTN=-2    COULD NOT FIND SYMBOL AMONG KNOWN ELEMENTS
C   IRTN=-3    THE FORM OF STRING WAS NOT  'CA40' OR '40CA'
C            E.G.  IT MAY HAVE BEEN 40CA40  OR CA40CA
C
C
C     ***NOTE*** IF   IZ.NE.999 ON ENTRY...THE FOLLOWING SUBSTITUTIONS A
C                 ACCEPTED AS ONE BYTE STRINGS 'S':
C        G - GAMMA      RETURNS IZ=0 IA=0                                1850   
C        N - NEUTRON -  RETURNS IZ=0 IA=1
C        P - PROTON  -  RETURNS IZ=1 IA=1
C        D - DEUTERON   RETURNS IZ=1 IA=2
C        T - TRITON  -  RETURNS IZ=1 IA=3
C        H - HELIUM-3   RETURNS IZ=2 IA=3
C        A - ALPHA      RETURNS IZ=2 IA=4
C
C  THIS ROUTINE HAS A SEPARATE ENTRY: CODEZ
C     CALL CODEZ(IZ,SYMBOL)  RETURNS THE SYMBOL FOR ELEMENT Z=IZ
C     IF IZ<0 OR IZ>104  SYMBOL=BLANK   SYMBOL,AS ABOVE, IS TWO BYTES    1860   
C
C     CODED BY F. SERDUKE
C     5/8/75 - COMPILED FOR PTOLEMY;  ALLOW NP FOR N <= 9
C     9/27/75 - FIX ERROR IN NP CASE.
C     8/23/76 - CDC/IBM VERSION
C     12/26/79 - STANDARD MORTRAN - RPG
C     1/14/79 - INTEGER*2'S TO CHARACTER*2 - RPG
C     5/30/80 - REMOVE MULTIPLE COLONS FROM COMMENT - S.P.
C     7/10/80 - CHR, CWD PREFIXES - S.P.
C                                                                        1870   
C - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
C
C     DATA FOR SYMBOLS OF THE ELEMENTS
      PARAMETER ( NUMELS = 109 )
      CHARACTER*2 ELEMNT(NUMELS)
      DATA ELEMNT/'H ', 'HE', 'LI', 'BE', 'B ', 'C ', 'N ', 'O ', 'F ',
     *'NE', 'NA', 'MG', 'AL', 'SI', 'P ', 'S ', 'CL', 'AR', 'K ', 'CA',
     *'SC', 'TI', 'V ', 'CR', 'MN', 'FE', 'CO', 'NI', 'CU', 'ZN', 'GA',
     *'GE', 'AS', 'SE', 'BR', 'KR', 'RB', 'SR', 'Y ', 'ZR', 'NB', 'MO',
     *'TC', 'RU', 'RH', 'PD', 'AG', 'CD', 'IN', 'SN', 'SB', 'TE', 'I ',  1880   
     *'XE', 'CS', 'BA', 'LA', 'CE', 'PR', 'ND', 'PM', 'SM', 'EU', 'GD',
     *'TB', 'DY', 'HO', 'ER', 'TM', 'YB', 'LU', 'HF', 'TA', 'W ', 'RE',
     *'OS', 'IR', 'PT', 'AU', 'HG', 'TL', 'PB', 'BI', 'PO', 'AT', 'RN',
     *'FR', 'RA', 'AC', 'TH', 'PA', 'U ', 'NP', 'PU', 'AM', 'CM', 'BK',
     *'CF', 'ES', 'FM', 'MD', 'NO', 'LR', 'RF', 'HA', 'NH', 'NS', 'HS',
     &  'MT' /
      CHARACTER*2 SYM, SYMBOL, AP, AN, BLANK2
      DATA BLANK2/' '/
      DATA AN/'N'/
      DATA AP/'P'/                                                       1890   
      CHARACTER*1 SYM1(2),S(8)
      CHARACTER*1 BLNK1
      DATA BLNK1 / ' ' /
      CHARACTER*1 SIN(8)
      INTEGER SVAL(5)
      CHARACTER*1 ZSYM(2)
      EQUIVALENCE(SYM,SYM1(1))
C
C --- DEFINE THE SPECIAL CASES OF ONE-BYTE STRINGS
      INTEGER  NCASE / 7 /                                               1900   
      CHARACTER*2  SCASE(7) / 'G', 'N', 'P', 'D', 'T', 'H', 'A' /
      INTEGER  IZSC (7) /  0,0,1,1,1,2,2   /
      INTEGER  IASC (7) /  0,1,1,2,3,3,4   /
C
C  SET SYMBOL=BLANK...IDCODE IS NUMBER OF FIELDS DECODED..J POINTS
C  TO THE CURRENT BYTE BEING DECODED
      IDCODE=0
      IRTN = 0
      SYM1(2) = BLNK1
      SYM = BLANK2                                                       1910   
      SYMBOL = BLANK2
      J=1
      DO  I = 1, 8
         S(I) = SIN(I)
C     CONVERT TO UPPER CASE
         IF ( S(i) .GE. 'a' .AND. S(i) .LE. 'z' )
     &      S(i) = CHAR( ICHAR(S(i)) + ICHAR('A') - ICHAR('a') )
      enddo
      DO 53 I = 1, 5
 53   SVAL(I) = ICHAR(S(I)) - ICHAR('0')                                 1920   
C
C -- LOOK AT FIRST BYTE...IF NUMERIC..STRING IS OF FORM '40CA'
      IF ( SVAL(1).GE. 0 .AND. SVAL(1).LE. 9 ) GO TO 200
C
C -- J POINTS TO A NON-NUMERIC SYMBOL...
100   SYM1(1) = S(J)
      J = J+1
C
C -- CHECK SPECIAL CASES ON G,N,P,D,T,H,A
      IF ( IDCODE.EQ.0 .AND. IZ.NE.999 .AND. S(J).EQ.BLNK1 ) GO TO 902   1930   
C
C -- CHECK NEXT CHARACTER..IF NUMERIC OR BLANK..END OF SYMBOL FIELD
      IF ( SVAL(J).GE. 0 .AND. SVAL(J).LE. 9 .OR. S(J).EQ.BLNK1 )  GO TO
     * 110
C .......ELSE THIS IS A TWO CHARACTER SYMBOL
      SYM1(2) = S(J)
      J = J+1
C
C .. NOW LOOK AT CHARACTER FOLLOWING SYMBOL....
110   IF ( IDCODE.EQ.0 .AND. (SVAL(J).LT. 0 .OR. SVAL(J).GT. 9) )        1940   
     &   GO TO 901
C
C -- IF ELEMENT SYMBOL IS AT END..CHECK THAT NEXT CHAR IS BLANK..
      IF ( IDCODE.EQ.1 .AND. S(J).NE.BLNK1 ) GO TO 9003
C
C   NOW THE ELEMENT SYMBOL IS IN SYM...
      IDCODE = IDCODE+1
C     ......IF BOTH FIELDS DECODED...GO FIND 'IZ'
      IF ( IDCODE.GE.2 ) GO TO 300
C                                                                        1950   
C -- DECODE ATOMIC MASS...J-TH CHARACTER IS GUARANTEED NUMERIC
200   IA = SVAL(J)
210   J = J+1
C -- IF NEXT CHARACTER IS NUMERIC...PROCEED WITH 'IA' DECODE
      IF ( SVAL(J).GE. 0 .AND. SVAL(J).LE. 9 ) GO TO 220
C -- IF 2-ND FIELD AND NEXT CHAR IS BLANK...GO WRAP UP BY GETTING IZ
      IF ( IDCODE.GE.1 .AND. S(J).EQ.BLNK1 ) GO TO 300
C -- IF 2-ND FIELD AND NEXT CHAR IS NON-NUMERIC...BUGGERED STRING
      IF ( IDCODE.GE.1 .AND. (SVAL(J).LT. 0.OR.SVAL(J).GT. 9) )
     &   GO TO 9003                                                      1960   
C --  FALL THRU MEANS WE ARE DONE WITH FIRST FIELD OF THE FORM '40CA'
C     AND WE HAVE THE IA=40...NOW GO DECODE SYM
      IDCODE= IDCODE+1
      GO TO 100
C
220   IA = 10*IA + SVAL(J)
      GO TO 210
C
C  HAVE SYM....GET ITS 'IZ'
300   IZZ=0                                                              1970   
      SYMBOL=SYM
C
C ... IF IT IS NP WITH N <= 9 THEN IT IS N PROTONS
      IF ( IA .LE. 9  .AND.  SYM .EQ. AP )  GO TO 380
C ... IF SYM='N'...AND IA<=9...IT IS NEUTRON...ELSE NITROGEN
      IF ( IA.LE.9 .AND. SYM.EQ.AN ) GO TO 390
C
C .... LOOP ON ELEMNT TABLE TO GET Z OF SYM
      DO 310 IZZ=1,NUMELS
      IF ( SYM.EQ.ELEMNT(IZZ) ) GO TO 390                                1980   
310   CONTINUE
C ---- FALL THRU MEANS SOME SNAFU...
      GO TO 9002
C
 380  IZ = IA
      RETURN
C
390   IZ = IZZ
      RETURN
C                                                                        1990   
C - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
C    E N T R Y    F O R    C O D E Z:
C   GIVEN  IZ,  IT RETURNS THE SYMBOL IN TWO BYTES
C - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
      ENTRY CODEZ(IZ,ZSYM)
C
      SYM = BLANK2
      IF ( IZ.LT.0 .OR. IZ.GT.NUMELS ) GO TO 490
      SYM = AN
      IF ( IZ.EQ.0 ) GO TO 490                                           2000   
      SYM = ELEMNT(IZ)
490   ZSYM(1) = SYM1(1)
      ZSYM(2) = SYM1(2)
      RETURN
C
C -- ABNORMAL CONDITIONS
901   IRTN = -1
C --- ONLY A SYMBOL WAS CODED...IZ IS RETURNED BUT IA=-1
      GO TO 300
C                                                                        2010   
C -- HANDLE SPECIAL CASES HERE.....
902   SYMBOL=SYM
      DO 909 N=1,NCASE
      IF ( SYM .EQ. SCASE(N) ) GO TO 911
909   CONTINUE
C --- IF NONE OF THESE...GO TO DECODE SYM WITH IA=-1 ON RETURN
         GO TO 901
911   IA = IASC(N)
      IZ = IZSC(N)
      RETURN                                                             2020   
C
9002  IRTN = -2
      RETURN
9003  IRTN = -3
      RETURN
      END
